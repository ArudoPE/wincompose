namespace WinCompose
{
    /// <summary>
    /// Identifies a page of the GUI.
    /// </summary>
    public enum GuiPage
    {
        /// <summary>
        /// Identifies an empty page.
        /// </summary>
        None,

        /// <summary>
        /// Identifies the page displaying sequences.
        /// </summary>
        Sequences,

        /// <summary>
        /// Identifies the page displaying settings.
        /// </summary>
        Settings,
    }
}